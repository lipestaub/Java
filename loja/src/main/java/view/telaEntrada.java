    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 
        Historico h = new Historico();
        
        HistoricoDAO dao = new HistoricoDAO();
        
        h.setDescricaoProduto(jComboBoxProdutoEntrada.getSelectedItem().toString());   
        h.setQuantidade(Integer.parseInt(jTextQuantidadeEntrada.getText()));
        
        dao.entrada(h);
        
        jComboBoxProdutoEntrada.setSelectedItem("Selecionar...");
        
        jTextQuantidadeEntrada.setText("");
        
        JOptionPane.showMessageDialog(null, "Entrada registrada!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened

        jComboBoxProdutoEntrada.addItem("Selecionar...");
        
        ProdutoDAO dao = new ProdutoDAO();
        
        Iterator itr = dao.listarProdutos().iterator();
        
        while(itr.hasNext()){
            
            jComboBoxProdutoEntrada.addItem(itr.next().toString());
            
        }
        
        jTextData.setText(now().toString());
        
    }//GEN-LAST:event_formWindowOpened

