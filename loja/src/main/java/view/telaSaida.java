    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     
        Historico h = new Historico();
        
        HistoricoDAO dao = new HistoricoDAO();
        
        h.setDescricaoProduto(jComboBoxProdutoSaida.getSelectedItem().toString());   
        h.setQuantidade(Integer.parseInt(jTextQuantidadeSaida.getText()));
        
        Iterator itr = dao.controleEstoque(h).iterator();
        
        while(itr.hasNext()){
            
            estoque = Integer.parseInt(itr.next().toString());
            
        }
        
        saida = Integer.parseInt(jTextQuantidadeSaida.getText());
        
        if(saida > estoque){
            
            JOptionPane.showMessageDialog(null, "Estoque insuficiente.", "Erro", JOptionPane.ERROR_MESSAGE);
            
        }else{
        
            dao.saida(h);
            
            JOptionPane.showMessageDialog(null, "Saída registrada!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
            
        }
        
        jComboBoxProdutoSaida.setSelectedItem("Selecionar...");
        
        jTextQuantidadeSaida.setText("");
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened

        jComboBoxProdutoSaida.addItem("Selecionar...");
        
        ProdutoDAO dao = new ProdutoDAO();
        
        Iterator itr = dao.listarProdutos().iterator();
        
        while(itr.hasNext()){
            
            jComboBoxProdutoSaida.addItem(itr.next().toString());
            
        }
        
        jTextData.setText(now().toString());
        
    }//GEN-LAST:event_formWindowOpened

